package com.Loops;

public class WhileDemo {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 10)
        {
            System.out.println(i);
            i++;
        }
        i = 1;
        while (i <= 10)
        {
            System.out.println("Ruchi");
            i++;
        }

        i = 10;
        while(i >= 1)
        {
            System.out.println(i);
            i--;
        }

    }
}
